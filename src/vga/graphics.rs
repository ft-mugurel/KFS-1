use core::sync::atomic::{AtomicBool, Ordering};

use crate::vga::text_mod::out;
use crate::x86::io::{inb, outb};

pub const VGA_WIDTH: u16 = 320;
pub const VGA_HEIGHT: u16 = 200;
const VGA_BUFFER: usize = 0xA0000;

static IN_GRAPHICS: AtomicBool = AtomicBool::new(false);

pub fn is_in_graphics_mode() -> bool {
    IN_GRAPHICS.load(Ordering::Relaxed)
}


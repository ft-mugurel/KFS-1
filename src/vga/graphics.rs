unsafe fn set_palette() {
    outb(0x3C8, 0);
    outb(0x3C9, 0);
    outb(0x3C9, 0);
    outb(0x3C9, 0);
    for i in 1..256u16 {
        let i = i as u8;
        outb(0x3C8, i);
        let r = ((i as u32 * 9) & 0x3F) as u8;
        let g = ((i as u32 * 3) & 0x3F) as u8;
        let b = ((i as u32 * 7) & 0x3F) as u8;
        outb(0x3C9, r);
        outb(0x3C9, g);
        outb(0x3C9, b);
    }
}

pub fn put_pixel(x: u16, y: u16, color: u8) {
    if x < VGA_WIDTH && y < VGA_HEIGHT {
        unsafe {
            let offset = (y as usize) * (VGA_WIDTH as usize) + (x as usize);
            *((VGA_BUFFER + offset) as *mut u8) = color;
        }
    }
}

pub fn draw_fractol() {
    unsafe {
        set_mode_13h();
        set_palette();
    }

    const MAX_ITER: u32 = 100;
    let w = VGA_WIDTH as f32;
    let h = VGA_HEIGHT as f32;
    // Mandelbrot view: classic zoom
    let xmin = -2.0f32;
    let xmax = 1.0f32;
    let ymin = -1.2f32;
    let ymax = 1.2f32;
    let dx = (xmax - xmin) / w;
    let dy = (ymax - ymin) / h;

    for py in 0..VGA_HEIGHT {
        let y0 = ymin + (py as f32) * dy;
        for px in 0..VGA_WIDTH {
            let x0 = xmin + (px as f32) * dx;
            let mut x = 0.0f32;
            let mut y = 0.0f32;
            let mut iter = 0u32;
            while x * x + y * y <= 4.0 && iter < MAX_ITER {
                let x2 = x * x;
                let y2 = y * y;
                y = 2.0 * x * y + y0;
                x = x2 - y2 + x0;
                iter += 1;
            }
            let color = if iter == MAX_ITER { 0u8 } else { (iter % 255 + 1) as u8 };
            put_pixel(px, py, color);
        }
    }

    IN_GRAPHICS.store(true, Ordering::Relaxed);
}

pub unsafe fn exit_graphics_mode() {
    set_mode_03h();
    out::render_active_screen();
    IN_GRAPHICS.store(false, Ordering::Relaxed);
}
